package com.srk.pkg;

public class Result {
	 private int policy;
	    private String text;
	 
	    public Result(int policy, String text) {
	        super();
	        this.policy = policy;
	        this.text = text;
	    }
	 
	    public int getPolicy() {
	        return policy;
	    }
	 
	    public void setPolicy(int id) {
	        this.policy = id;
	    }
	 
	    public String getText() {
	        return text;
	    }
	 
	    public void setText(String name) {
	        this.text = name;
	    }
	
	
	
	
}
